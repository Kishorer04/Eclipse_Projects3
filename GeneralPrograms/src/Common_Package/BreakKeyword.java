package Common_Package;

public class BreakKeyword {

	public static void main(String[] args) {
		
		for(int i=0;i<5;i++) {
			
			int num= 1;
			
			switch(num)
			{
			case 0: 
			System.out.println("Hi");
			break;
			
			case 1:
			System.out.println("Hi 1");
			break;
			
			case 2:
			System.out.println("Hi 2");
			
			
			}
			
			
		}

	}

}
